// Function to display the superheroes in the table
function displaySuperheroes() {
    const tableBody = document.getElementById('superheroesTable').getElementsByTagName('tbody')[0];

    // Clear the existing table rows
    tableBody.innerHTML = '';

    // Loop through the superheroes array and add a row for each one
    superheroes.forEach((superhero) => {
        const row = tableBody.insertRow();

        const nameCell = row.insertCell();
        nameCell.textContent = superhero.name;

        const ageCell = row.insertCell();
        ageCell.textContent = superhero.age;

        const countryCell = row.insertCell();
        countryCell.textContent = superhero.country;

        const mobileCell = row.insertCell();
        mobileCell.textContent = superhero.mobile;

        const emailCell = row.insertCell();
        emailCell.textContent = superhero.email;
    });
}

// Function to search for a superhero by name
function searchSuperheroes() {
    const searchTerm = document.getElementById('searchInput').value.toLowerCase();
    const tableBody = document.getElementById('superheroesTable').getElementsByTagName('tbody')[0];

    // Loop through the table rows and hide the ones that don't match the search term
    for (let i = 0; i < tableBody.rows.length; i++) {
        const row = tableBody.rows[i];
        const nameCell = row.cells[0];
        const name = nameCell.textContent.toLowerCase();

        if (name.includes(searchTerm)) {
            row.style.display = '';
        } else {
            row.style.display = 'none';
        }
    }
}

// Call the displaySuperheroes function to display the superheroes when the page loads
window.onload = displaySuperheroes;

// Add an event listener to the search button to call the searchSuperheroes function when it's clicked
document.getElementById('searchButton').addEventListener('click', searchSuperheroes);